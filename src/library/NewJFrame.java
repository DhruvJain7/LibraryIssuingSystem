    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         DefaultTableModel model
	= (DefaultTableModel)jTable1.getModel();
try {
	Class.forName("com.mysql.jdbc.Driver");
	         con = DriverManager.getConnection(
		"jdbc:mysql://localhost:3306/lib", "root",
		"LordTesla@77");
	         String query = "SELECT* FROM RECORD;";
	          rs = stmt.executeQuery(query);
	while (rs.next()) {
		String nof= rs.getString("Name of Faculty");
		String nob = rs.getString("Name of book");
		String id = rs.getString("Issuing Date");
		String rd = rs.getString("Returning Date");
		//String adr = rs.getString("address");
		model.addRow(
			new Object[] { nof, nob, id, rd});
	}
	rs.close();
	stmt.close();
	con.close();
        }
catch (Exception e) {
	JOptionPane.showMessageDialog(this,
								"Error In Connectivity");
}
    }//GEN-LAST:event_jButton2ActionPerformed

